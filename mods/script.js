function generateModBox(mod) {
    return `
        <a href="${mod.modUrl}" target="_blank" class="mod-box">
            <img src="${mod.imgSrc}" alt="image" class="mod-img">
            <div class="mod-content">
                <h2 class="mod-name">${mod.modName}</h2>
                <hr class="mod-divider">
                <p class="mod-description">${mod.modDescription}</p>
            </div>
        </a>
    `;
}

function populateMods() {
    document.getElementById('building-and-interior').innerHTML = mods.buildingAndInterior.map(generateModBox).join('');
    document.getElementById('content-expansion').innerHTML = mods.contentExpansion.map(generateModBox).join('');
    document.getElementById('creatures-and-characters').innerHTML = mods.creaturesAndCharacters.map(generateModBox).join('');
    document.getElementById('gameplay-mechanics').innerHTML = mods.gameplayMechanics.map(generateModBox).join('');
    document.getElementById('modding-tools').innerHTML = mods.moddingTools.map(generateModBox).join('');
    document.getElementById('visuals-and-customization').innerHTML = mods.visualsAndCustomization.map(generateModBox).join('');
}

document.addEventListener('DOMContentLoaded', populateMods);

function openModal(modalId) {
    document.getElementById(modalId).style.display = 'block';
}

function closeModal(modalId) {
    document.getElementById(modalId).style.display = 'none';
}

// Close the modal if the user clicks anywhere outside of it
window.onclick = function(event) {
    var modals = document.getElementsByClassName('modal');
    for (var i = 0; i < modals.length; i++) {
        if (event.target == modals[i]) {
            modals[i].style.display = "none";
        }
    }
}
